	private void checkFileUser() throws IOException {
		String url = getClass().getClassLoader().getResource("./Juegos/").getPath();
		url = url + "default.txt";
		url = url.substring(1, url.length());
		try {
			File file = new File(url);
			file.createNewFile();
		} catch (IOException e) {
			e.printStackTrace();
		}
	}

